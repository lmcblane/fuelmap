"""## 4 · Assign colours based on a linear price scale"""

import matplotlib.colors as mcolors
import matplotlib.pyplot as plt
import numpy as np

# Build a 3-stop linear colour map: green → yellow → red
cmap = mcolors.LinearSegmentedColormap.from_list(
    "fuel_price",
    [COLOR_LOW, COLOR_MID, COLOR_HIGH]
)

price_min = df["e10_p"].min()
price_max = df["e10_p"].max()

def price_to_hex(price: float) -> str:
    """Map a price value linearly onto the colour scale."""
    if price_max == price_min:
        t = 0.5
    else:
        t = (price - price_min) / (price_max - price_min)
    rgba = cmap(t)
    return mcolors.to_hex(rgba)

df["color"] = df["e10_p"].apply(price_to_hex)

# Quick preview of the colour scale
fig, ax = plt.subplots(figsize=(6, 0.6))
gradient = np.linspace(0, 1, 256).reshape(1, -1)
ax.imshow(gradient, aspect="auto", cmap=cmap)
ax.set_xticks([0, 128, 255])
ax.set_xticklabels([f"{price_min:.1f}p", f"{(price_min+price_max)/2:.1f}p", f"{price_max:.1f}p"])
ax.set_yticks([])
ax.set_title("Colour scale: E10 price per litre (pence)")
plt.tight_layout()
plt.show()

