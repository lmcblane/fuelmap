def parse_station(station: dict, retailer: str) -> dict | None:
    """Extract the fields we need from a station record.
    Returns None if E10 price or coordinates are missing."""
    try:
        fuels = station.get("prices", {})

        # E10 may be keyed as 'E10', 'unleaded', or 'petrol' depending on retailer
        e10 = (
            fuels.get("E10")
            or fuels.get("unleaded")
            or fuels.get("petrol")
        )
        if e10 is None:
            return None

        lat = float(station.get("location", {}).get("latitude", 0)
                    or station.get("lat", 0))
        lon = float(station.get("location", {}).get("longitude", 0)
                    or station.get("lng", 0)
                    or station.get("long", 0))

        if lat == 0 and lon == 0:
            return None

        name = (
            station.get("site_name")
            or station.get("name")
            or station.get("store_name")
            or retailer
        )

        # Grab both address and postcode, then combine them so we can search!
        addr = station.get("address") or ""
        postcode = station.get("postcode") or ""
        address = f"{addr} {postcode}".strip()

        return {
            "name":     name,
            "retailer": retailer,
            "address":  address,
            "lat":      lat,
            "lon":      lon,
            "e10_p":    float(e10),   # price in pence per litre
        }
    except (TypeError, ValueError, KeyError):
        return None

import urllib3
# Suppress the warnings Python throws when you disable SSL verification
urllib3.disable_warnings(urllib3.exceptions.InsecureRequestWarning)

def fetch_retailer(name: str, url: str) -> list[dict]:
    """Download one retailer feed and return a list of parsed station dicts."""
    try:
        # Added verify=False to bypass expired SSL certificates!
        r = requests.get(url, headers=HEADERS, timeout=TIMEOUT, verify=False)
        r.raise_for_status()
        data = r.json()
    except Exception as e:
        print(f"  ⚠  {name}: skipped ({e})")
        return []

    # Different retailers structure their JSON differently
    stations_raw = (
        data.get("stations")
        or data.get("sites")
        or data.get("store_locations")
        or data.get("data")
        or (data if isinstance(data, list) else [])
    )

    parsed = [parse_station(s, name) for s in stations_raw]
    results = [p for p in parsed if p is not None]
    print(f"  ✓  {name}: {len(results)} stations with E10")
    return results

