# ── Step 3: Filter to Scotland via Postcode and clean the data ──────────────
import re

# Regex pattern for Scottish postcode areas using non-capturing groups (?:)
# Uses a negative lookahead to safely exclude TD12 and TD15 (which are in England).
scotland_regex = r'\b(?:AB|DD|DG|EH|G|HS|IV|KA|KW|KY|ML|PA|PH|TD(?!(?:12|15)\b)|ZE)\d'

# Filter by searching the address column for the postcode pattern (case-insensitive)
df = df_all[
    df_all["address"].str.contains(scotland_regex, flags=re.IGNORECASE, regex=True, na=False)
].copy()

# Sanity check: drop outlier prices (< 50p or > 300p per litre)
df = df[(df["e10_p"] >= 50) & (df["e10_p"] <= 300)]

# If prices are stored as integers (e.g. 14990 = 149.90p), normalise them
if df["e10_p"].median() > 500:
    df["e10_p"] = df["e10_p"] / 100

df = df.drop_duplicates(subset=["lat", "lon"]).reset_index(drop=True)

