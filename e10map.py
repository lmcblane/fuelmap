"""## 5 · Generate Interactive Folium Map

This uses OpenStreetMap to plot interactive grouped markers across Scotland. Hovering over a price bubble reveals the station details.
"""

# ── Step 5: Interactive Map ───────────────────────────────────────────────────
import folium
from folium.plugins import MarkerCluster

# Create a base map centered on Scotland using OpenStreetMap
m = folium.Map(
    location=[57.5, -4.0],
    zoom_start=6,
    tiles="CartoDB positron"
)

# Initialize the marker cluster
marker_cluster = MarkerCluster(
    name="E10 Fuel Prices"
).add_to(m)

# Iterate through the dataframe to build markers
for idx, row in df.iterrows():

    # Create a custom HTML icon to display the price directly
    price_html = f"""
    <div style="
        background-color: {row['color']};
        padding: 4px;
        border-radius: 4px;
        border: 1px solid #333;
        font-weight: bold;
        font-family: sans-serif;
        text-align: center;
        width: 45px;
        box-shadow: 2px 2px 4px rgba(0,0,0,0.3);
        font-size: 12px;">
        {row['e10_p']:.1f}p
    </div>
    """

    icon = folium.DivIcon(html=price_html)

    # Build the tooltip content
    tooltip_html = (
        f"<b>{row['name']}</b><br>"
        f"{row['retailer']}<br>"
        f"{row['address']}"
    )

    # Add the marker to the cluster
    folium.Marker(
        location=[row['lat'], row['lon']],
        icon=icon,
        tooltip=tooltip_html
    ).add_to(marker_cluster)

